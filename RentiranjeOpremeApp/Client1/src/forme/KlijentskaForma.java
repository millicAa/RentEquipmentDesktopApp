    void ubaciStavku(StavkaRentiranja sr) {
        ModelTabele mt = (ModelTabele) tabelaStavki.getModel();
        mt.ubaciStavku(sr);
        srediStavke();
    }

    private void srediStavke() {
        String datOd = txtDatumOd.getText();
        String datDo = txtDatumDo.getText();
        
        Date datumOd = null;
        Date datumDo = null;
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        
        try {
            datumOd = sdf.parse(datOd);
        } catch (ParseException ex) {
        }
        try {
            datumDo = sdf.parse(datDo);
        } catch (ParseException ex) {
        }
        
        if(datumDo != null && datumOd != null){
            ModelTabele mt = (ModelTabele) tabelaStavki.getModel();
            mt.srediStavke(datumOd,datumDo);
            
        }
        
    }
