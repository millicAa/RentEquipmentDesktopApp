    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        if(txtDatumDo.getText().isEmpty() || txtDatumOd.getText().isEmpty() || txtKlijent.getText().isEmpty()){
            JOptionPane.showMessageDialog(this , "ne sme biti prazno");
            return;
        }
        
        String datOd = txtDatumOd.getText();
        String datDo = txtDatumDo.getText();
        
        Date datumOd = null;
        Date datumDo = null;
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        
        try {
            datumOd = sdf.parse(datOd);
            
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this , "Moras uneti datum od");
            return;
        }
        try {
            datumDo = sdf.parse(datDo);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this , "Moras uneti datum do");
            return;
        }
        
        long diff = datumDo.getTime() - datumOd.getTime();
        long dani = TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS);
        
        if(dani < 1){
            JOptionPane.showMessageDialog(this , "Mora vise od jednog dana");
            return;
        }
        
        if(datumOd.after(datumDo)){
            JOptionPane.showMessageDialog(this , "Od < Do");
            return;
        }
        String klijent = txtKlijent.getText();
        ModelTabele mt = (ModelTabele) tabelaStavki.getModel();
        srediStavke();
        ArrayList<StavkaRentiranja> lista = mt.getLista();
        
        if(lista.size()<1){
            JOptionPane.showMessageDialog(this , "mORA LISTA > 0");
            return;
        }
        
        double iznos = 0;
        
        for (StavkaRentiranja stavkaRentiranja : lista) {
            iznos += stavkaRentiranja.getIznos();
        }
        
        Rentiranje r = new Rentiranje(-1, klijent, datumOd, datumDo, iznos, lista);
        
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.SACUVAJ);
        kz.setParametar(r);
        
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        
        JOptionPane.showMessageDialog(this, so.getPoruka());
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

